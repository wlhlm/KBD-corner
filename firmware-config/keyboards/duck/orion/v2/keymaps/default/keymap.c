bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  // If console is enabled, it will print the matrix position and status of each key pressed
#ifdef CONSOLE_ENABLE
	if (record->event.pressed) uprintf("KL: kc: 0x%04X, col: %2u, row: %2u, pressed: %u, time: %5u\n", keycode, record->event.key.col, record->event.key.row, record->event.pressed, record->event.time);
#endif
  return true;
}
